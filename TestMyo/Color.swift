import UIKit

internal extension UIColor {
    static var barGrey: UIColor {
        return UIColor(red: 36.0/255.0, green: 36.0/255.0, blue: 36.0/255.0, alpha: 1.0)
    }
    static var lighterGrey: UIColor {
        return UIColor(red: 77.0/255.0, green: 77.0/255.0, blue: 77.0/255.0, alpha: 1.0)
    }
    static var itemOrange: UIColor {
        return UIColor(red: 255.0/255.0, green: 58.0/255.0, blue: 0.0/255.0, alpha: 1.0)
    }
}
